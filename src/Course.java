public class Course {

    private String name;
    private int number;

    public Course() {
        this.name = "Bilinmiyor";
        this.number = 0;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }
}
